package modelo;

public class Servicio {
    private String ser_codigo;
    private String ser_tipo;
    private double ser_precio;

    public String getSer_codigo() {
        return ser_codigo;
    }

    public void setSer_codigo(String ser_codigo) {
        this.ser_codigo = ser_codigo;
    }

    public String getSer_tipo() {
        return ser_tipo;
    }

    public void setSer_tipo(String ser_tipo) {
        this.ser_tipo = ser_tipo;
    }

    public double getSer_precio() {
        return ser_precio;
    }

    public void setSer_precio(double ser_precio) {
        this.ser_precio = ser_precio;
    }
    
}
